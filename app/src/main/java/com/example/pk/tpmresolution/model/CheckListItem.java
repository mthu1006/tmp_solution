public class CheckListItem {
    String checklist_id;
    String checklist_name;
    String checklist_date;
    String checklist_status_id;
    String checklist_status_name;
    String checklist_user_name;
    String checklist_action_name;

    public String getChecklist_id() {
        return checklist_id;
    }

    public void setChecklist_id(String checklist_id) {
        this.checklist_id = checklist_id;
    }

    public String getChecklist_name() {
        return checklist_name;
    }

    public void setChecklist_name(String checklist_name) {
        this.checklist_name = checklist_name;
    }

    public String getChecklist_date() {
        return checklist_date;
    }

    public void setChecklist_date(String checklist_date) {
        this.checklist_date = checklist_date;
    }

    public String getChecklist_status_id() {
        return checklist_status_id;
    }

    public void setChecklist_status_id(String checklist_status_id) {
        this.checklist_status_id = checklist_status_id;
    }

    public String getChecklist_status_name() {
        return checklist_status_name;
    }

    public void setChecklist_status_name(String checklist_status_name) {
        this.checklist_status_name = checklist_status_name;
    }

    public String getChecklist_user_name() {
        return checklist_user_name;
    }

    public void setChecklist_user_name(String checklist_user_name) {
        this.checklist_user_name = checklist_user_name;
    }

    public String getChecklist_action_name() {
        return checklist_action_name;
    }

    public void setChecklist_action_name(String checklist_action_name) {
        this.checklist_action_name = checklist_action_name;
    }
}
